package main.java.need.vo;

public class PathVO {

	private String rootPath; 
	private String javaRootPath;
	private String xmlRootPath;
	private String viewRootPath;
	private String tableName;
	private String fileName;
	private String folderName;
	
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getFolderName() {
		return folderName;
	}
	public void setFolderName(String folderName) {
		this.folderName = folderName;
	}
	public String getRootPath() {
		return rootPath;
	}
	public void setRootPath(String rootPath) {
		this.rootPath = rootPath;
	}
	public String getJavaRootPath() {
		return javaRootPath;
	}
	public void setJavaRootPath(String javaRootPath) {
		this.javaRootPath = javaRootPath;
	}
	public String getXmlRootPath() {
		return xmlRootPath;
	}
	public void setXmlRootPath(String xmlRootPath) {
		this.xmlRootPath = xmlRootPath;
	}
	public String getViewRootPath() {
		return viewRootPath;
	}
	public void setViewRootPath(String viewRootPath) {
		this.viewRootPath = viewRootPath;
	}
	public String getTableName() {
		return tableName;
	}
	public void setTableName(String tableName) {
		this.tableName = tableName;
	}
	
	
	
}
